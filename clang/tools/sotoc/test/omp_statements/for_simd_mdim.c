int main(void) {

  int i;
  int j;
  int x[5][10] = {{2,3,5,7,11,13,17,23,29,31},{2,3,5,7,11,13,17,23,29,31},{2,3,5,7,11,13,17,23,29,31},{2,3,5,7,11,13,17,23,29,31},
  {2,3,5,7,11,13,17,23,29,31}};
  int y[5][10] = {{31,29,23,17,13,11,7,5,3,2},{31,29,23,17,13,11,7,5,3,2},{31,29,23,17,13,11,7,5,3,2},{31,29,23,17,13,11,7,5,3,2},
  {31,29,23,17,13,11,7,5,3,2}};
  int a = 5;
  int z[5][10];

  #pragma omp target device(0) map(tofrom: x,y) map(alloc: z)
  #pragma omp parallel for simd
  for (i = 0; i < 5; i++) {
    for (j = 0; j < 10; j++) {
      z[i][j] = x[i][j] + a*y[i][j];
    }
  }


  return 0;
}
